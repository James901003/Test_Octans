    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
    
        
        
        
        int row = TablaDatos.getSelectedRow();
        if (row == -1) {     //
  
            JOptionPane.showMessageDialog(null, "No se Selecciono");
        } 
           else {
            id = Integer.parseInt((String) TablaDatos.getValueAt(row, 0).toString());
            String Rol = (String) TablaDatos.getValueAt(row, 1);
            String nom = (String) TablaDatos.getValueAt(row, 2);
            String Activo = (String) TablaDatos.getValueAt(row, 3);
            
            btn_si.setSelected(false);
            btn_no.setSelected(false); 
                    
            txtId.setText("" + id);
            txtDNI.setText(Rol);
            
            
       
            txtNom.setText(nom);
           
            if (Activo.equals("SI")){
            
                btn_si.setSelected(true); 
                
            } else {
                
                btn_no.setSelected(true); 
            }
            
            
            

                }
    }//GEN-LAST:event_TablaDatosMouseClicked

